let currentLang = localStorage.getItem('appLang') || 'zh';

window.t = key => (translations[currentLang] && translations[currentLang][key]) || key;

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('appLang', lang);

  document.documentElement.lang = lang === 'en' ? 'en' : 'zh-Hant';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (translations[lang][key]) {
      el.setAttribute('aria-label', translations[lang][key]);
    }
  });

  document.querySelectorAll('#lang-toggle, .cms-lang-toggle').forEach(function (langBtn) {
    langBtn.textContent = lang === 'zh' ? 'EN' : '中文';
  });

  document.dispatchEvent(new CustomEvent('langchange'));

  if (typeof window.syncAppNavWidth === 'function') {
    window.syncAppNavWidth();
  }
}

function toggleLanguage() {
  const newLang = currentLang === 'zh' ? 'en' : 'zh';
  applyLanguage(newLang);
}

// Dark Mode Logic
function applyTheme(isDark) {
  if (isDark) {
    document.body.classList.add('dark-theme');
  } else {
    document.body.classList.remove('dark-theme');
  }
  localStorage.setItem('darkTheme', isDark ? 'true' : 'false');
  document.querySelectorAll('#theme-toggle').forEach(function (btn) {
    btn.textContent = isDark ? '☀️' : '🌙';
    btn.setAttribute('aria-label', isDark ? '切換淺色模式' : '切換深色模式');
  });
}

function toggleTheme() {
  const isDark = !document.body.classList.contains('dark-theme');
  applyTheme(isDark);
}

function initTheme() {
  const saved = localStorage.getItem('darkTheme');
  if (saved === 'false') {
    applyTheme(false);
  } else {
    applyTheme(true);
  }
}

document.addEventListener('DOMContentLoaded', () => {
  initTheme();

  applyLanguage(currentLang);

  document.querySelectorAll('#theme-toggle').forEach(function (btn) {
    btn.addEventListener('click', toggleTheme);
  });

  document.querySelectorAll('#lang-toggle, .cms-lang-toggle').forEach(function (langBtn) {
    langBtn.addEventListener('click', toggleLanguage);
  });
});
