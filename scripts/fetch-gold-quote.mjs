#!/usr/bin/env node
/**
 * Fetch latest BOT gold quote and write data/gold-quote.json + js/gold-quote-data.js.
 * Usage: npm run fetch:gold
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const { fetchBotGoldQuote } = require('../lib/bot-gold-quote.js');

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'data', 'gold-quote.json');
const JS_OUT = path.join(ROOT, 'js', 'gold-quote-data.js');

async function main() {
  const payload = await fetchBotGoldQuote();
  const { quote } = payload;

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, JSON.stringify({ quote: quote, alloyRates: payload.alloyRates }, null, 2) + '\n', 'utf8');

  const jsBody = '/* Auto-generated by npm run fetch:gold — embedded BOT quote fallback. */\n'
    + 'window.__GOLD_QUOTE_BOOTSTRAP__ = ' + JSON.stringify({ quote: quote, alloyRates: payload.alloyRates }, null, 2) + ';\n';
  fs.writeFileSync(JS_OUT, jsBody, 'utf8');

  console.log('Wrote', OUT);
  console.log('Wrote', JS_OUT);
  console.log('XAU/g:', quote.sell, 'stamp:', quote.bot_posted_at);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
